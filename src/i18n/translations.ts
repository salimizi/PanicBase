export const TRANSLATIONS: Record<Locale, Messages> = {
  en,
  fr,
};

export function defaultLocale(): Locale {
  return 'en';
}

/** Locale persistée (hors React) — même logique que `I18nProvider` au montage. */
const BROWSER_LOCALE_MAP: Record<string, Locale> = {
  en: 'en',
  fr: 'fr',
};

export function localeFromNavigator(): Locale | null {
  if (typeof navigator === 'undefined') return null;
  const lang = (navigator.language || '').toLowerCase().split('-')[0];
  return BROWSER_LOCALE_MAP[lang] ?? null;
}

function normalizeLocale(raw: string | null): Locale | null {
  if (raw === 'en' || raw === 'fr') return raw;
  return null;
}

export function readStoredLocale(): Locale {
  try {
    const raw = localStorage.getItem(LOCALE_STORAGE_KEY);
    const locale = normalizeLocale(raw);
    if (locale) return locale;
    if (raw) {
      const migrated: Locale = 'en';
      localStorage.setItem(LOCALE_STORAGE_KEY, migrated);
      return migrated;
    }
  } catch {
    /* ignore */
  }
  return localeFromNavigator() ?? defaultLocale();
}

export function translateKey(locale: Locale, key: string, vars?: Record<string, string | number>): string {
  const table = TRANSLATIONS[locale] ?? TRANSLATIONS.en;
  let s = table[key] ?? TRANSLATIONS.en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      const token = `{{${k}}}`;
      s = s.split(token).join(String(v));
    }
  }
  return s;
}
