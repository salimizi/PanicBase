export function localizeDiagnosticText(text: string, locale: Locale): string {
  if (!text || locale === 'fr') return text;
  const mapping = locale === 'en' ? FR_TO_EN_ORDERED : null;
  if (!mapping) return text;
  let out = text.replace(/\u2019/g, "'").replace(/\u2018/g, "'");
  // Two passes: some replacements expose nested French fragments.
  for (let pass = 0; pass < 2; pass++) {
    for (const [fr, translated] of mapping) {
      if (out.includes(fr)) {
        out = out.split(fr).join(translated);
      } else {
        // Try first-char-uppercase variant (lowercase pattern → capitalized in text, e.g. sentence start)
        const frCap = fr.charAt(0).toUpperCase() + fr.slice(1);
        if (frCap !== fr && out.includes(frCap)) {
          const trCap = translated.charAt(0).toUpperCase() + translated.slice(1);
          out = out.split(frCap).join(trCap);
        } else {
          // Try first-char-lowercase variant (uppercase pattern → lowercase mid-sentence)
          const frLow = fr.charAt(0).toLowerCase() + fr.slice(1);
          if (frLow !== fr && out.includes(frLow)) {
            const trLow = translated.charAt(0).toLowerCase() + translated.slice(1);
            out = out.split(frLow).join(trLow);
          }
        }
      }
    }
  }
  return out;
}
